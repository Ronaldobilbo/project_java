    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
try {
            sql="select * from customer where idcustomer='"+
            Cid.getSelectedItem()+"'";
            stat=con.createStatement();
            rs=stat.executeQuery(sql);
            if (rs.next()) {
                Tid.setText(rs.getString("idcustomer"));
                Tnama.setText(rs.getString("namacustomer"));
                Talamat.setText(rs.getString("alamat"));
                 Cid.setVisible(false);
                Tid.setVisible(true);
                JOptionPane.showMessageDialog(rootPane, "ini data nya");
            }else {
                Cid.setVisible(true);
                Tid.setVisible(false);
                Cid.setSelectedIndex(0);
                Tid.setText("");
                Tnama.setText("");
                Tid.setText("");
                 Cid.setSelectedIndex(0);
                JOptionPane.showMessageDialog(rootPane, "data belum di input");
            }
        }    
      catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(rootPane, "error" +e);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_searchActionPerformed

